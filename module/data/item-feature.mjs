import SynthicideItemBase from './base-item.mjs';
import SYNTHICIDE from '../helpers/config.mjs';

const FEATURE_TYPES = ['bioclass','aspect'];

/**
 * Shared logic for any item that occupies the single‑feature slot on an actor.
 * Bioclass and Aspect are thin subclasses that provide their own subtype data.
 */
export default class SynthicideFeature extends SynthicideItemBase {
  static LOCALIZATION_PREFIXES = [
    'SYNTHICIDE.Item.base',
    'SYNTHICIDE.Item.Feature'
  ];

  /**
   * Build the shared schema for both bioclasses and aspects.
   */
  static defineSchema() {
    const { fields } = foundry.data;
    const schema = super.defineSchema();

    schema.featureType = new fields.StringField({
      required: true,
      choices: FEATURE_TYPES,
      initial: 'bioclass'
    });

    schema.traits = new fields.ArrayField(
      new fields.SchemaField({
        sort: new fields.NumberField({ required: true, integer: true, initial: 0 }),
        name: new fields.StringField({ required: true, initial: '' }),
        description: new fields.HTMLField({ required: true, initial: '' })
      }),
      { initial: [] }
    );

    return schema;
  }

  /**
   * Return a default trait array for a feature based on its system data.
   * Subclasses or presets supply the actual values.
   * @param {object} system - the system data being created/updated
   * @returns {Array<object>}
   */
  static getDefaultTraits({ featureType, bioclassType, aspectType } = {}) {
    switch (featureType) {
      case 'bioclass': {
        const preset = SYNTHICIDE.getBioclassPreset(bioclassType || 'skinbag');
        return preset.traits.map(t => ({
          sort: t.sort,
          key: t.key || '',
          name: t.key ? game.i18n.localize(`SYNTHICIDE.Item.Bioclass.Trait.${t.key}.Name`) : '',
          description: t.key
            ? game.i18n.localize(`SYNTHICIDE.Item.Bioclass.Trait.${t.key}.Description`)
            : ''
        }));
      }
      case 'aspect': {
        const preset = SYNTHICIDE.aspectPresets[aspectType] || {};
        return (preset.traits || []).map(t => ({
          sort: t.sort,
          name: t.name || '',
          description: t.description || ''
        }));
      }
      default:
        return [];
    }
  }

  /**
   * Override create to ensure default traits are populated when a new
   * feature is instantiated without an explicit traits array.
   */
  static async create(data = {}, options = {}) {
    const system = foundry.utils.mergeObject({}, data.system || {}, { inplace: false });
    if (!system.traits?.length) {
      system.traits = this.getDefaultTraits(system);
    }
    data = foundry.utils.mergeObject(data, { system }, { inplace: false });
    return super.create(data, options);
  }

  /**
   * Treat either a typed feature or a legacy bioclass item as a bioclass.
   */
  get isBioclass() {
    return this.featureType === 'bioclass' || this.parent?.type === 'bioclass';
  }
  /**
   * Likewise recognise the new "aspect" item type in addition to
   * the feature/subtype form.
   */
  get isAspect() {
    return this.featureType === 'aspect' || this.parent?.type === 'aspect';
  }

  /**
   * Retrieve a localized title for this feature depending on subtype.
   */
  get localizedTitle() {
    const key = `SYNTHICIDE.Item.Feature.Title.${this.featureType}`;
    return game.i18n.localize(key);
  }

  /**
   * Resolve the owning actor from this embedded item data model.
   * @returns {Actor|null}
   * @private
   */
  _getOwningActor(options = {}) {
    const optionParent = options?.parent;
    if (optionParent?.documentName === 'Actor') return optionParent;
    return this.parent?.actor ?? optionParent?.actor ?? null;
  }

  /**
   * Determine whether this client should execute local side effects.
   * Some Foundry paths provide no userId in data model hooks.
   * @param {string|undefined|null} userId
   * @returns {boolean}
   * @private
   */
  _isCurrentUser(userId) {
    return !userId || game.userId === userId;
  }

  // ---------------------------------------------------------------------
  // Actor integration helpers
  // ---------------------------------------------------------------------

  /**
   * Apply this feature to an actor by creating trait items and syncing
   * any special attributes.  The actor may be provided explicitly or is
   * inferred from the owning document.
   * @param {Actor} [actor]
   */
  async applyToActor(actor) {
    if (!this.featureType) return;
    const owning = actor ?? this._getOwningActor();
    if (!owning) return;
    await this._removeFeatureTraits(owning);
    await this._createFeatureTraits(owning);
    await this._syncFeatureAttributes(owning);
  }

  /**
   * Remove any trait items on the actor that were generated by this
   * feature.  Traits are identified by matching the featureType field.
   * @private
   */
  async _removeFeatureTraits(owningActor) {
    const featureType = this.featureType;
    if (!featureType) return;
    const toDelete = owningActor.items
      .filter(i => i.type === 'trait' && i.system.traitType === featureType)
      .map(i => i.id);
    if (toDelete.length) {
      await owningActor.deleteEmbeddedDocuments('Item', toDelete);
      // optional debug output keyed by feature type (e.g. synthicideBioclass)
      const flag = `synthicide${featureType.charAt(0).toUpperCase() + featureType.slice(1)}`;
      const debug = Boolean(SYNTHICIDE.debug?.[flag]);
      if (debug) {
        console.groupCollapsed(`[Synthicide] ${featureType} traits deleted: ${this.parent?.name}`);
        console.table(toDelete);
        console.groupEnd();
      }
    }
  }

  /**
   * Create trait items and embed them on the owning actor.  Each trait
   * record is copied from the feature's system data and tagged with the
   * appropriate traitType so it can later be cleaned up.
   * @private
   */
  async _createFeatureTraits(owningActor) {
    const featureType = this.featureType;
    if (!featureType) return;
    const traits = Array.isArray(this.traits) ? this.traits : [];
    if (!traits.length) return;

    const docs = traits.map(trait => {
      const prefix = featureType.charAt(0).toUpperCase() + featureType.slice(1);
      const name = trait.key
        ? game.i18n.localize(`SYNTHICIDE.Item.${prefix}.Trait.${trait.key}.Name`)
        : trait.name || 'Trait';
      const description = trait.key
        ? game.i18n.localize(`SYNTHICIDE.Item.${prefix}.Trait.${trait.key}.Description`)
        : trait.description || '';
      const system = {
        ...trait,
        name,
        description,
        traitType: featureType
      };
      if (this.isBioclass) system.bioClassLink = true;
      return { type: 'trait', name, system };
    });

    await owningActor.createEmbeddedDocuments('Item', docs);
  }

  /**
   * Route attribute synchronization to whatever specialised handler exists.
   * Only bioclasses currently need this behaviour, so the base method is
   * deliberately lean; subclasses implement the actual work if desired.
   * @private
   */
  async _syncFeatureAttributes(owningActor) {
    if (this.isBioclass && typeof this._syncBioclassAttributes === 'function') {
      await this._syncBioclassAttributes(owningActor);
    }
  }

  /**
   * Subclass extension point for subtype-specific cleanup when a feature is
   * deleted from an actor.
   * @param {Actor} owningActor
   * @private
   */
  async _cleanupOnDelete(_owningActor) {
    // default no-op; bioclass subclass implements attribute resets
  }

  /**
   * Foundry hook: creation should apply the feature to its actor.
   */
  async _onCreate(data, options, userId) {
    super._onCreate(data, options, userId);
    if (!this._isCurrentUser(userId)) return;
    const actor = this._getOwningActor(options);
    if (actor) {
      try {
        await this.applyToActor(actor);
      } catch (err) {
        if (SYNTHICIDE.debug?.synthicideBioclass) console.error('applyToActor failed', err);
      }
    }
  }

  /**
   * Foundry hook: update may require re-applying traits/attributes.
   */
  async _onUpdate(changed, options, userId) {
    super._onUpdate(changed, options, userId);
    if (!this._isCurrentUser(userId)) return;
    const actor = this._getOwningActor(options);
    if (!actor) return;

    const traitUpdate = !!changed?.system?.traits;
    const subtypeChange = Boolean(changed?.system?.featureType || changed?.system?.bioclassType || changed?.system?.aspectType);
    if (traitUpdate || subtypeChange) {
      await this.applyToActor(actor);
    }
  }

  /**
   * Capture actor before embedded item is removed so post-delete logic still
   * has access to the owning document.
   */
  async _preDelete(options, userId) {
    const allowed = await super._preDelete?.(options, userId);
    if (allowed === false) return false;
    this._deletingActor = this._getOwningActor(options);
    return allowed;
  }

  /**
   * Foundry hook: deleting a feature should remove any generated traits.
   */
  async _onDelete(options, userId) {
    super._onDelete(options, userId);
    if (!this._isCurrentUser(userId)) return;
    const actor = this._deletingActor ?? this._getOwningActor(options);
    this._deletingActor = null;
    if (!actor) return;

    await this._removeFeatureTraits(actor);
    await this._cleanupOnDelete(actor);

    if (typeof actor.aggregateAndApplyItemModifiers === 'function') {
      await actor.aggregateAndApplyItemModifiers({
        debug: Boolean(SYNTHICIDE.debug?.synthicideModifiers)
      });
    }
  }

  async _preUpdate(changes, options, user) {
    const allowed = await super._preUpdate?.(changes, options, user);
    if (allowed === false) return false;

    if (changes.system) {
      const subtypeChanged =
        !changes.system.traits &&
        (changes.system.featureType || changes.system.bioclassType || changes.system.aspectType);
      if (subtypeChanged) {
        const sample = {...this.system, ...changes.system};
        changes.system.traits = SynthicideFeature.getDefaultTraits(sample);
      }
    }
    return allowed;
  }
}

